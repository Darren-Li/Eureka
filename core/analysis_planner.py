from core.llm_client import call_llm

def generate_analysis_plan(probe, user_prompt):
    plan_prompt = f"""
    你是一位经验丰富、专业严谨的数据分析师，擅长为任意类型的数据设计清晰、可执行的分析计划。

    ### 任务
    基于以下数据特征和用户分析要求，生成一份**详细且实操性强**的分析计划。

    ### 输入信息
    - 数据特征：{probe}
    - 用户分析要求：{user_prompt}

    ### 输出要求
    请严格按照以下结构输出，使用 Markdown 格式：

    1. **分析目标**  
       提炼用户核心需求，明确本次分析要解决的核心问题或要达成的洞察目标（1-2段文字）。

    2. **分析步骤**  
       分点列出详细步骤，每一步必须包含：
       - 步骤名称（简洁有力）
       - 具体分析内容
       - 使用的主要字段或数据部分
       - 预期输出形式（例如：表格、统计指标、图表描述等）
       步骤要逻辑清晰、循序渐进，具有强可执行性，避免模糊描述。

    3. **建议分析方法和可视化**  
       - 推荐使用的统计/分析方法（例如描述性统计、相关性分析、聚类、回归等）
       - 推荐的图表类型（优先使用 seaborn 或 matplotlib 实现）
       - 每种图表说明其目的和预期展示的洞察

    ### 额外要求
    - 完全适配任意数据类型（数值、类别、文本、时间序列等），不要假设具体业务场景。
    - 所有步骤必须具体、可直接在 Python 中执行。
    - 优先选择实用、高效的方法和常见图表。
    - 输出只包含以上 1、2、3 三个部分，不要添加额外解释或寒暄。

    现在，请开始生成分析计划。
    """

    return call_llm(plan_prompt)